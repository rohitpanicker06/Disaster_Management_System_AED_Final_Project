    private void populateMedicineTable(ArrayList<Medicine> medicineDirectory, String searchBy,String searchText) {
      
        
         ArrayList<Medicine> medicine = new ArrayList<>();
         if(searchBy.equals("Name")){
     for(Medicine md: medicineDirectory)
     {
         if(md.getName().equalsIgnoreCase(searchText))
         {
             medicine.add(md);
         }
     }
         }
         else{
             medicineDirectory.stream().filter(md -> (Integer.parseInt(searchText) == md.getMedicineId())).forEachOrdered(md -> {
                 medicine.add(md);
             });
         }
    }

    private void populateMedicineTableInitially() {
        
         DefaultTableModel tableModel = (DefaultTableModel) tblRecords.getModel();
        tableModel.setRowCount(0);
        
        
        for(Medicine med: MedicineDirectory.medicineDirectory)
        {
            
           Object[] row = new Object[3];
                row[0] = med;
                row[1] = med.getName();
                row[2] = med.getPrice();
                tableModel.addRow(row);
        }
        
        
     
    }
