    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        
        int selectedRowIndex = tblRecords.getSelectedRow();
        if (selectedRowIndex == -1) {
            JOptionPane.showMessageDialog(this, "No Medicine is selected, Please Try Again");
            return;
        } 
        
        Medicine medicine = (Medicine) tblRecords.getValueAt(selectedRowIndex, 0);
        
        medicineComboBox.addItem(medicine.toString());
        prescriptionResult.add(medicine);
        
    }//GEN-LAST:event_addBtnActionPerformed

